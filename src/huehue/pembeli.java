    private void autokodepembeli(){
        try{
            String sql;
            sql = "SELECT COUNT(kode_pembeli) as jumlah FROM pembeli";
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            if (rs.next()) {
                String jumlah = rs.getString("jumlah");
                int jumint = Integer.parseInt(String.valueOf(jumlah));
                if (jumint > 0) {
                    st = cn.createStatement();
                        sql = "SELECT MAX(kode_pembeli) AS kode FROM pembeli";
                        rs2 = st.executeQuery(sql);
                        if(rs2.next()){
                            
                            String id = rs2.getString("kode").substring(2);
                            String kode = String.valueOf(Integer.parseInt(id) + 1);
                            if(kode.length() == 1){
                               tkdpembeli.setText("KP00"+kode);
                            }else if(kode.length() == 2){
                                tkdpembeli.setText("KP0"+kode);
                            }else
                                tkdpembeli.setText("KP"+kode);
                        }
                }else{
                    tkdpembeli.setText("KP001");
                }
            }
        }catch(SQLException e){
        
        }
    }
    
