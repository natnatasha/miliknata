    private void lsimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lsimpanMouseClicked
        // TODO add your handling code here:
        if (tkdpembeli.getText().equals("") || tnama.getText().equals("") || tnohp.getText().equals("") || talamat.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Silahkan lengkapi data!");
        } else {
             try{
                st = cn.createStatement();
                st.executeUpdate("insert into pembeli set " + "kode_pembeli='" + tkdpembeli.getText() + "', nama='" + tnama.getText()
                + "', no_hp='" + tnohp.getText() + "', alamat='" + talamat.getText()+ "'");
                tampildata();
                reset();
                autokodepembeli();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            } catch(Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_lsimpanMouseClicked

