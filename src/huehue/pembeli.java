    public void tampildata(){
        try {
            st = cn.createStatement();
            tabmodel.getDataVector().removeAllElements();
            tabmodel.fireTableDataChanged();
            rs = st.executeQuery("select * from pembeli");
            while(rs.next()){
                Object[] data = {
                    rs.getString("kode_pembeli"),
                    rs.getString("nama"),
                    rs.getString("no_hp"),
                    rs.getString("alamat"),};
                tabmodel.addRow(data);
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }
    
